  # ● 取得中心點
  #--------------------------------------------------------------------------
  def get_ox(battler, number, ori_width)
    unless PICTURES_ORIGIN.include?(battler.name)
       return (ori_width/2)
     end
    if (PICTURES_ORIGIN[battler.name][number] != nil) && (PICTURES_ORIGIN[battler.name][number][0] != nil)
      # 圖片有左右翻轉
      if self.mirror
        return ori_width - PICTURES_ORIGIN[battler.name][number][0]
      else 
        return PICTURES_ORIGIN[battler.name][number][0]
      end   
    else
      return (ori_width/2)
    end
  end
